function iso2ToFlag(iso2: string): string {
  if (!iso2) return '';
  const code = iso2.toUpperCase();
  if (code.length !== 2) return '';
  // Regional Indicator Symbol Letter A starts at 0x1F1E6 (decimal 127462)
  // Unicode math: 127397 + charCode of 'A'..'Z'
  try {
    return String.fromCodePoint(
      ...code.split('').map(ch => 127397 + ch.charCodeAt(0))
    );
  } catch (e) {
    return '';
  }
}

export const COUNTRIES: Country[] = RAW_COUNTRIES.map(c => ({
  ...c,
  flag: iso2ToFlag(c.iso2),
}));

export function getCountryByIso2(iso2: string): Country | undefined {
  return COUNTRIES.find(c => c.iso2.toLowerCase() === iso2.toLowerCase());
}

export function getCountryByDialCode(dial: string): Country | undefined {
  return COUNTRIES.find(c => c.dialCode === dial.replace(/^\+/, ''));
}
